</div>
<p id="section4">Après avoir appréhendé les rotations de la Terre et les saisons qui changent au fur et à mesure,vous
    avez un quiz caché en haut à gauche de la page. En cliquant dessus, un quiz apparaîtra, et vous pourrez y
    répondre.</p>
