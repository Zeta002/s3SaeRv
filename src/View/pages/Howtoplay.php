</div>
<p>Grâce aux sliders à droite, vous pourrez voir la rotation de la Terre autour du soleil sur 365 jours.</p>
<p>Au-dessus du slider, vous pouvez sélectionner le mois et aussi le jour.</p>
<div id="centered-image">
    <img src="https://media.discordapp.net/attachments/802682110913151016/1196756321815498843/image.png?ex=65b8c92d&is=65a6542d&hm=2def7e2a25061dda9b1276c696dfc5ea979dd09b094c529ada2a4dbf79f559e2&=&format=webp&quality=lossless"
