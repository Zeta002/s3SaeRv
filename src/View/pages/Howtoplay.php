<p>Après le chargement Unity, vous aurez un environnement interactif. Vous pouvez changer de vue grâce à un bouton
    situé...</p>
<div id="centered-image">
    <img src="" alt="image 1">
</div>
<p>Grâce aux sliders à gauche, vous pourrez voir la rotation de la Terre autour du soleil sur 24 heures.</p>
<div id="centered-image">
    <img src="https://media.discordapp.net/attachments/802682110913151016/1196755481956450394/image.png?ex=65b8c865&is=65a65365&hm=81dbbb4a5d9822fab8cfc373f7340f8696e9d8ecc6d5fd3cc79356de6540d5e0&=&format=webp&quality=lossless"
